#include <stdio.h>
int main(){
	unsigned int i = 7;
	printf(" int i : %u \n", i);
	char c = 'e';
	printf(" char c : %c \n", c);
					
	float f  = 7.5;
	printf(" float f : %f \n", f);

	double d  = 987.789995;
	printf(" double d : %0.3f \n", d);
					
	short s = 7;
	printf(" short s : %d \n", s);
					
	long l = 7;
	printf(" long l : %ld \n", l);
	
	return 0;
}
